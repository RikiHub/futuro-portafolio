public class ExamenEscrito extends Examen{

    private int duracion;
    private int nota;

    public ExamenEscrito() {

    }

    public int getDuracion() {
        return duracion;
    }

    public void setDuracion(int duracion) {
        this.duracion = duracion;
    }

    public int getNota() {
        return nota;
    }

    public void setNota(int nota) {
        this.nota = nota;
    }
}
